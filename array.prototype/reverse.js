// reverse()反转数组。会修改数组
var numbers = [1, 2, 3, 4, 5];
var r = numbers.reverse();
console.log(r);//[ 5, 4, 3, 2, 1 ]
console.log(numbers);//[ 5, 4, 3, 2, 1 ]

var colors = ['black', 'white', 'red'];
var r1 = colors.reverse();
console.log(r1);//[ 'red', 'white', 'black' ]
console.log(colors);//[ 'red', 'white', 'black' ]